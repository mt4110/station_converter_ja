async function readTextIfExists(filePath) {
  try {
    return await readFile(filePath, "utf8");
  } catch (error) {
    if (error && typeof error === "object" && "code" in error && error.code === "ENOENT") {
      return null;
    }

    throw error;
  }
}

async function verifyGeneratedArtifacts(tempSchemaOutputPath, sdkSource) {
  const expectedSchema = await readFile(tempSchemaOutputPath, "utf8");
  const [currentSchema, currentSdk] = await Promise.all([
    readTextIfExists(schemaOutputPath),
    readTextIfExists(sdkOutputPath)
  ]);

  const staleFiles = [];

  if (currentSchema !== expectedSchema) {
    staleFiles.push(path.relative(frontendRoot, schemaOutputPath));
  }

  if (currentSdk !== sdkSource) {
    staleFiles.push(path.relative(frontendRoot, sdkOutputPath));
  }

  if (staleFiles.length > 0) {
    throw new Error(
      `Generated station SDK files are stale. Run \`npm run generate:station-sdk\` and commit the updated artifacts:\n- ${staleFiles.join("\n- ")}`
    );
  }
}

async function main() {
  const openApiDocument = await loadOpenApiDocument();

  const tempDir = await mkdtemp(path.join(os.tmpdir(), "station-openapi-"));
  const tempOpenApiPath = path.join(tempDir, "openapi.json");
  const tempSchemaOutputPath = path.join(tempDir, "station-openapi.ts");
  const sdkSource = generateSdkSource();

  try {
    await writeFile(tempOpenApiPath, JSON.stringify(openApiDocument, null, 2));
    execFileSync(openapiTypescriptBin(), [tempOpenApiPath, "-o", tempSchemaOutputPath], {
      cwd: frontendRoot,
      stdio: "inherit"
    });

    if (CHECK_MODE) {
      await verifyGeneratedArtifacts(tempSchemaOutputPath, sdkSource);
      console.log("Station SDK artifacts are up to date.");
      return;
    }

    await mkdir(generatedDir, { recursive: true });
    await copyFile(tempSchemaOutputPath, schemaOutputPath);
    await writeFile(sdkOutputPath, sdkSource);
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }

  console.log(`Generated ${path.relative(frontendRoot, schemaOutputPath)} and ${path.relative(frontendRoot, sdkOutputPath)}.`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
